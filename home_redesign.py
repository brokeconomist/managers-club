import streamlit as st

def show_home_redesign(selected_tool_state):
    st.title("📊 Managers’ Club")
    st.markdown("""
Καλωσήρθατε στο Managers’ Club!  
Εδώ θα βρείτε όλα τα χρηματοοικονομικά και επιχειρησιακά εργαλεία για να διαχειριστείτε τις επιχειρήσεις σας με ευκολία και αποτελεσματικότητα.  
Διαλέξτε την κατηγορία που σας ενδιαφέρει για να ξεκινήσετε.
""")

    def tool_button(label, tool_name):
        if st.button(label):
            selected_tool_state['tool'] = tool_name

    # --- Ανάλυση Πωλήσεων ---
    with st.expander("👥 Ανάλυση Πωλήσεων", expanded=True):
        tool_button("🟢 Νεκρό Σημείο (Break-Even)", "🟢 Νεκρό Σημείο (Break-Even)")
        tool_button("⚙️ Αλλαγή Νεκρού Σημείου", "⚙️ Αλλαγή Νεκρού Σημείου (Τιμή / Κόστος / Επένδυση)")
        tool_button("👥 CLV - Αξία Πελάτη", "👥 CLV - Αξία Πελάτη")
        tool_button("🔄 Ανάλυση Υποκατάστασης Προϊόντων", "🔄 Ανάλυση Υποκατάστασης Προϊόντων")
        tool_button("➕ Ανάλυση Συμπληρωματικών Προϊόντων", "➕ Ανάλυση Συμπληρωματικών Προϊόντων")

    # --- Διαχείριση Κεφαλαίου ---
    with st.expander("💰 Διαχείριση Κεφαλαίου", expanded=False):
        tool_button("📉 Όριο Απώλειας Πωλήσεων", "📉 Όριο Απώλειας Πωλήσεων πριν τη Μείωση Τιμών")
        tool_button("🕒 Ανάλυση Αύξησης Πίστωσης", "🕒 Ανάλυση Αύξησης Πίστωσης")
        tool_button("🏛️ Ανάλυση Πολιτικής Πίστωσης", "🏛️ Ανάλυση Πολιτικής Πίστωσης")
        tool_button("🏦 Ανάλυση Έκπτωσης Πληρωμής Προμηθευτών", "🏦 Ανάλυση Έκπτωσης Πληρωμής Προμηθευτών Τοις Μετρητοίς")
        tool_button("💰 Ανάλυση NPV Για Έκπτωση Πληρωμής", "💰 Ανάλυση NPV Για Έκπτωση Πληρωμής Τοις Μετρητοίς")

    # --- Παραγωγή & Απόδοση ---
    with st.expander("🏭 Παραγωγή & Απόδοση", expanded=False):
        tool_button("⚖️ Μέσο Κόστος Παραγωγής Ανά Μονάδα", "⚖️ Μέσο Κόστος Παραγωγής Ανά Μονάδα")
        tool_button("📦 Οικονομικότερη Παραγγελία Εμπορευμάτων", "📦 Οικονομικότερη Παραγγελία Εμπορευμάτων")
        tool_button("🔁 Ταχύτητα Κυκλοφορίας Αποθεμάτων", "🔁 Ταχύτητα Κυκλοφορίας Αποθεμάτων (ποσότητα/αξία)")
        tool_button("📈 Υπολογισμός Μικτού Κέρδους", "📈 Υπολογισμός Μικτού Κέρδους")

    # --- Χρηματοοικονομικά / Δανεισμός ---
    with st.expander("🏦 Χρηματοοικονομικά / Δανεισμός", expanded=False):
        tool_button("🏡 Ανάλυση Δανείου vs Leasing", "🏡 Ανάλυση Δανείου vs Leasing")
        tool_button("📊 Μέσος Ταμειακός Κύκλος", "📊 Μέσος Ταμειακός Κύκλος")
        tool_button("⚖️ Μεσοσταθμικός Υπολογισμός Ημερών Πίστωσης", "⚖️ Μεσοσταθμικός Υπολογισμός Ημερών Πίστωσης")

