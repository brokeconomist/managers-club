# home_redesign.py
import streamlit as st

def show_homepage():
    st.title("🏠 Καλώς ήρθατε στο Managers’ Club")
    st.write("""
        Επιλέξτε μία κατηγορία εργαλείων για να δείτε τα διαθέσιμα modules. 
        Κάθε κατηγορία περιέχει σχετικά εργαλεία για οικονομική και επιχειρηματική ανάλυση.
    """)

    # Ορισμός κατηγοριών εργαλείων
    tool_groups = {
        "📊 Ανάλυση Κερδών": [
            "Νεκρό Σημείο (Break-Even)",
            "Αλλαγή Νεκρού Σημείου (Τιμή / Κόστος / Επένδυση)",
            "Υπολογισμός Μικτού Κέρδους",
        ],
        "👥 Πελάτες & CLV": [
            "CLV - Αξία Πελάτη",
            "Ανάλυση Υποκατάστασης Προϊόντων",
            "Ανάλυση Συμπληρωματικών Προϊόντων",
        ],
        "💳 Πιστωτική Πολιτική & Πληρωμές": [
            "Όριο Απώλειας Πωλήσεων πριν τη Μείωση Τιμών",
            "Ανάλυση Αύξησης Πίστωσης",
            "Ανάλυση Πολιτικής Πίστωσης",
            "Ανάλυση Έκπτωσης Πληρωμής Προμηθευτών Τοις Μετρητοίς",
            "Μεσοσταθμικός Υπολογισμός Ημερών Πίστωσης",
        ],
        "🏦 Δάνεια & Leasing": [
            "Ανάλυση Δανείου vs Leasing",
        ],
        "📦 Αποθέματα & Παραγγελίες": [
            "Οικονομικότερη Παραγγελία Εμπορευμάτων",
            "Ταχύτητα Κυκλοφορίας Αποθεμάτων (ποσότητα/αξία)",
        ],
        "🧾 Άλλα Εργαλεία": [
            "Μέσος Ταμειακός Κύκλος",
            "Μέσο Κόστος Παραγωγής Ανά Μονάδα",
            "Ανάλυση NPV Για Έκπτωση Πληρωμής Τοις Μετρητοίς",
        ],
    }

    # Δημιουργία tabs για κάθε κατηγορία
    tabs = st.tabs(list(tool_groups.keys()))
    for tab, (group_name, tools) in zip(tabs, tool_groups.items()):
        with tab:
            st.subheader(group_name)
            for tool in tools:
                st.write(f"• {tool}")

    st.info("Χρησιμοποιήστε τα tabs για να δείτε όλα τα εργαλεία και να επιλέξετε αυτό που σας ενδιαφέρει.")
